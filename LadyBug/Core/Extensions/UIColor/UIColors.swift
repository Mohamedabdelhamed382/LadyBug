import UIKit

extension UIColor {
    @nonobjc class var midGreen: UIColor {
        return UIColor(red: 75.0/255.0, green: 174.0/255.0, blue: 79.0/255.0, alpha: 1.0)
    }
    
    @nonobjc class var silver: UIColor {
        return UIColor(red: 196.0/255.0, green: 209.0/255.0, blue: 212.0/255.0, alpha: 1.0)
    }
}
